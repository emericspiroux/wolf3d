#include "libft.h"

void	*ft_memset(void *ptr, int value, size_t n)
{
	unsigned char	*pp;

	if (n > 0)
	{
		pp = (unsigned char *)ptr;
		while (n--)
			pp[n] = value;
	}
	return (ptr);
}
