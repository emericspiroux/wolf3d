#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*mem;

	mem = malloc(sizeof(size));
	if (!mem)
		return (NULL);
	ft_bzero(mem, size);
	return (mem);
}
