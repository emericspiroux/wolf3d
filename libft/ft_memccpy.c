#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	unsigned char		*dp;
	const unsigned char	*sp;

	if (n > 0)
	{
		dp = dest;
		sp = src;
		while (n--)
		{
			if ((*dp++ = *sp++) == c)
				return (dp);
		}
	}
	return (NULL);
}
